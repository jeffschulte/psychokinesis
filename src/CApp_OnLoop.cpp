void CApp::OnLoop() {

    if(movingleft) {
        Mainlocx--;
    }
    if(movingright) {
        Mainlocx++;
    }

    yvel -= 0.1;

    Mainlocy -= yvel;
    if(Mainlocy > 405) {
        Mainlocy = 405;
        yvel = 0;
    }

}

